s32 adventureStaticData::getStepIndex(u32 p1) {
    for (s32 i = 0, stepIndex = 0; i < getAllStepNum(); i++, stepIndex++) {
        if (p1 == s_adventureStaticData[i].unk0) {
            return stepIndex;
        }
    }
    return -1;
}

u32 adventureStaticData::getAllStepNum() {
    return AllStepNum;
}
